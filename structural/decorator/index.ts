/*
  Visão geral do Decorator

  - Agregar responsabilidade adicionais a um objeto dinamicamente. Os Decorators fornecem uma alternativa
    flexível ao uso de subclasses para extensão de funcionalidades.

  Abstração e Implementação

  - Abstração é um código de alto nível que geralmente delega ações para outro objeto.
  - Implementação é o código que realmente faz o trabalho.
*/